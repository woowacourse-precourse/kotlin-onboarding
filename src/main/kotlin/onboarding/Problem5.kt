package onboarding

fun solution5(money: Int): List<Int> {
    var input = money
    val unitOfMoney = listOf(50000, 10000, 5000, 1000, 500, 100, 50, 10, 1)
    val result = IntArray(9)
    for (i in unitOfMoney.indices) {
        result[i] = input / unitOfMoney[i]
        input %= unitOfMoney[i]
    }
    return result.toList()
}
